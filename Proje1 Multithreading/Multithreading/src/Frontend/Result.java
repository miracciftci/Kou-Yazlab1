    public void UrunleriGoruntule(){
        model.setRowCount(0);
        business.AramaGenel(Giris.veriler, MainMenu.threadSayisi,MainMenu.miktar,MainMenu.sutun);
        System.out.println("result: "+ThreadOrnek.result.size());
        
        if(ThreadOrnek.result != null){
            for(Data data : ThreadOrnek.result){
                Object[] eklenecek = {data.getId(),data.getProduct(),data.getIssue(),data.getCompany(),data.getState(),data.getZipCode(),data.getComplaintId()};
                if(model.getRowCount() == 25000){
                    break;
                }
                model.addRow(eklenecek);
            }
            
        }
        
        jLabelSure.setText(String.valueOf(Business.sure));
        veriSayisi.setText(String.valueOf(ThreadOrnek.result.size()));            
    }

